package org.joel.content.dto;

public class BuyInfo {
	private int id;  //产品id
	private int number;  //购买数量
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
}
